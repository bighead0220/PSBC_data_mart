  }

# ------ Below are VSQL scripts ----------
  print VSQL <<ENDOFINPUT;

\\set ON_ERROR_STOP on

--Step0:
DELETE FROM dw_sdata.FSS_001_FD_NETVALUE WHERE start_dt>=DATE('${TX_DATE_YYYYMMDD}');
UPDATE dw_sdata.FSS_001_FD_NETVALUE SET end_dt=DATE('2100-12-31') WHERE end_dt>=DATE('${TX_DATE_YYYYMMDD}') AND end_dt<>DATE('2100-12-31');

--Step1:
CREATE LOCAL TEMPORARY TABLE  T_194 ON COMMIT PRESERVE ROWS AS SELECT * FROM dw_sdata.FSS_001_FD_NETVALUE WHERE 1=0;

--Step2:
INSERT  INTO T_194 (
  AGENTCORPCODE,
  BUSINESSKINDCODE,
  PROCLIAMDATE,
  CURRENTFLAG,
  NETVALUE,
  OPENVALUE,
  SUBSCRIBEVALUE,
  APPLYVALUE,
  REDEEMVALUE,
  TRUSTEESHIPVALUE,
  INFOCHANGEVALUE,
  FUNDINCOMEUNIT,
  FUNDINCOMERATIO,
  UPDATEDATE,
  ACCUMULATIVENAV,
  start_dt,
  end_dt)
SELECT
  N.AGENTCORPCODE,
  N.BUSINESSKINDCODE,
  N.PROCLIAMDATE,
  N.CURRENTFLAG,
  N.NETVALUE,
  N.OPENVALUE,
  N.SUBSCRIBEVALUE,
  N.APPLYVALUE,
  N.REDEEMVALUE,
  N.TRUSTEESHIPVALUE,
  N.INFOCHANGEVALUE,
  N.FUNDINCOMEUNIT,
  N.FUNDINCOMERATIO,
  N.UPDATEDATE,
  N.ACCUMULATIVENAV,
  DATE('${TX_DATE_YYYYMMDD}'),
  DATE('2100-12-31')
FROM 
 (SELECT
  COALESCE(AGENTCORPCODE, '' ) AS AGENTCORPCODE ,
  COALESCE(BUSINESSKINDCODE, '' ) AS BUSINESSKINDCODE ,
  COALESCE(PROCLIAMDATE, '' ) AS PROCLIAMDATE ,
  COALESCE(CURRENTFLAG, '' ) AS CURRENTFLAG ,
  COALESCE(NETVALUE, 0 ) AS NETVALUE ,
  COALESCE(OPENVALUE, 0 ) AS OPENVALUE ,
  COALESCE(SUBSCRIBEVALUE, 0 ) AS SUBSCRIBEVALUE ,
  COALESCE(APPLYVALUE, 0 ) AS APPLYVALUE ,
  COALESCE(REDEEMVALUE, 0 ) AS REDEEMVALUE ,
  COALESCE(TRUSTEESHIPVALUE, 0 ) AS TRUSTEESHIPVALUE ,
  COALESCE(INFOCHANGEVALUE, 0 ) AS INFOCHANGEVALUE ,
  COALESCE(FUNDINCOMEUNIT, 0 ) AS FUNDINCOMEUNIT ,
  COALESCE(FUNDINCOMERATIO, 0 ) AS FUNDINCOMERATIO ,
  COALESCE(UPDATEDATE, '' ) AS UPDATEDATE ,
  COALESCE(ACCUMULATIVENAV, 0 ) AS ACCUMULATIVENAV 
 FROM  dw_tdata.FSS_001_FD_NETVALUE_${TX_DATE_YYYYMMDD}) N
LEFT JOIN
 (SELECT 
  AGENTCORPCODE ,
  BUSINESSKINDCODE ,
  PROCLIAMDATE ,
  CURRENTFLAG ,
  NETVALUE ,
  OPENVALUE ,
  SUBSCRIBEVALUE ,
  APPLYVALUE ,
  REDEEMVALUE ,
  TRUSTEESHIPVALUE ,
  INFOCHANGEVALUE ,
  FUNDINCOMEUNIT ,
  FUNDINCOMERATIO ,
  UPDATEDATE ,
  ACCUMULATIVENAV 
 FROM dw_sdata.FSS_001_FD_NETVALUE 
 WHERE END_DT = DATE('2100-12-31') ) T
ON N.AGENTCORPCODE = T.AGENTCORPCODE AND N.BUSINESSKINDCODE = T.BUSINESSKINDCODE AND N.PROCLIAMDATE = T.PROCLIAMDATE
WHERE
(T.AGENTCORPCODE IS NULL AND T.BUSINESSKINDCODE IS NULL AND T.PROCLIAMDATE IS NULL)
 OR N.CURRENTFLAG<>T.CURRENTFLAG
 OR N.NETVALUE<>T.NETVALUE
 OR N.OPENVALUE<>T.OPENVALUE
 OR N.SUBSCRIBEVALUE<>T.SUBSCRIBEVALUE
 OR N.APPLYVALUE<>T.APPLYVALUE
 OR N.REDEEMVALUE<>T.REDEEMVALUE
 OR N.TRUSTEESHIPVALUE<>T.TRUSTEESHIPVALUE
 OR N.INFOCHANGEVALUE<>T.INFOCHANGEVALUE
 OR N.FUNDINCOMEUNIT<>T.FUNDINCOMEUNIT
 OR N.FUNDINCOMERATIO<>T.FUNDINCOMERATIO
 OR N.UPDATEDATE<>T.UPDATEDATE
 OR N.ACCUMULATIVENAV<>T.ACCUMULATIVENAV
;

--Step3:
UPDATE dw_sdata.FSS_001_FD_NETVALUE P 
SET End_Dt=DATE('${TX_DATE_YYYYMMDD}')
FROM T_194
WHERE P.End_Dt=DATE('2100-12-31')
AND P.AGENTCORPCODE=T_194.AGENTCORPCODE
AND P.BUSINESSKINDCODE=T_194.BUSINESSKINDCODE
AND P.PROCLIAMDATE=T_194.PROCLIAMDATE
;

--Step4:
INSERT  INTO dw_sdata.FSS_001_FD_NETVALUE SELECT * FROM T_194;

COMMIT;

ENDOFINPUT

  close(VSQL);

  my $RET_CODE = $? >> 8;

  if ( $RET_CODE == 0 ) {
      return 0;
  }
  else {
      return 1;
  }
}

